package AST.CSS;

public class IdDefinerSelector extends Selector {
    private String id;

    public IdDefinerSelector(String id) {
        this.id = id;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    @Override
    public String toString() {
        return id;
    }
}
