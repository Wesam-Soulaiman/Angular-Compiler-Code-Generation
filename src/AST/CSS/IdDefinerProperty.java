package AST.CSS;

public class IdDefinerProperty extends CssProperty {
    private String id;

    public IdDefinerProperty(String id) {
        this.id = id;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    @Override
    public String toString() {
        return id;
    }
}
